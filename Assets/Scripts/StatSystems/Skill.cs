using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Skill : ModifiedStat
{
    private bool isKnown;

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public Skill()
    {
        isKnown = false;
        ExpToLevel = 25;
        LevelModifier = 1.1f;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public bool IsKnown { get; set; }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public enum SkillName
{
    Melee_Offence,
    Melee_Defence,
    Ranged_Offence,
    Ranged_Defence,
    Magic_Offence,
    Magic_Defence
}
